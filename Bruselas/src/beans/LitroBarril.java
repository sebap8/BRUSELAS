package beans;

public class LitroBarril {
	private String nombre;
	private int litros;
	private float barriles;
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getLitros() {
		return litros;
	}
	public void setLitros(int litros) {
		this.litros = litros;
	}
	public float getBarriles() {
		return barriles;
	}
	public void setBarriles(float barriles) {
		this.barriles = barriles;
	}

}
